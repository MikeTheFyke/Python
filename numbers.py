number = 2.5

print (float(number))
# equals 2.5
print (int(number))
# equals 2

number2 = 14

print (number + number2)

print (number - number2)

print (number * number2)

print (number / number2)

print (number // number2)
# Integer division - Round down so there are no decimal places
print (number ** number2)
# Exponentiation (number to the power of number2)